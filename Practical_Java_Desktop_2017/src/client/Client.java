    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        if (txtSearch.getText().isEmpty()) { //khung search trống, trả về toàn bộ danh sách
            m.setDataVector(data, header);
        } else {
            Vector searchResult = new Vector<>();
            for (int i = 0; i < data.size(); i++) {
                String model = ((Vector) data.get(i)).get(3).toString();
                if (model.equals(txtSearch.getText())) {
                    searchResult.add(data.get(i));
                }
            }
            m.setDataVector(searchResult, header);
        }
    }//GEN-LAST:event_btnSearchActionPerformed

