    private boolean validDetail() {
        if (txtCode.getText().trim().length() == 0) {
            return false;
        }
        if (txtManu.getText().trim().length() == 0) {
            return false;
        }
        try {
            int price = Integer.parseInt(txtPrice.getText().trim());
            if (price <= 0) {
                JOptionPane.showMessageDialog(this, "Invalid price");
                txtPrice.requestFocus();
                txtPrice.setBorder(redBorder);
                return false;
            } else {
                txtPrice.setBorder(defaultBorder);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Invalid price");
            return false;
        }
        try {
            int size = Integer.parseInt(txtSize.getText().trim());
            if (size <= 0) {
                JOptionPane.showMessageDialog(this, "Invalid size");
                txtSize.requestFocus();
                txtSize.setBorder(redBorder);
                return false;
            } else {
                txtSize.setBorder(defaultBorder);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Invalid size");
            return false;
        }
        
        return true;
    }

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        if (!addNew) {
            JOptionPane.showMessageDialog(this, "Nothing to save!");
            return;
        }
        if (!validDetail()) {
            JOptionPane.showMessageDialog(this, "Invalid pen!");
            return;
        }
        Vector row = new Vector();
        row.add(txtCode.getText().trim());
        row.add(txtManu.getText().trim());
        row.add(txtPrice.getText().trim());
        row.add(txtModel.getText().trim());
        row.add(txtSize.getText().trim());
        data.add(row);
        table.updateUI();
        txtCode.setEditable(false);
        txtManu.setEditable(false);
        txtModel.setEditable(false);
        txtPrice.setEditable(false);
        txtSize.setEditable(false);
        JOptionPane.showMessageDialog(this, "Save!");
    }//GEN-LAST:event_btnSaveActionPerformed

